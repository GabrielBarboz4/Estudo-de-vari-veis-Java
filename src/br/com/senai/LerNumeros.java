package br.com.senai;

public class LerNumeros {
    public static void main(String[] args) {
        double numero1 = 56;
        double numero2 = 234;

        double equacao1 = numero1 + numero2;
        double equacao2 = numero1 * numero2;
        double equacao3 = numero1 + numero1 / numero2;
        double equacao4 = numero2* (numero1 * numero1);

        System.out.println(equacao1);
        System.out.println(equacao2);
        System.out.println(equacao3);
        System.out.println(equacao4);
    }
}
